#include <stdio.h>
#include <stdlib.h>

float soma (float soma1, float soma2){
	float TotalSoma;
	TotalSoma = soma1 + soma2;
	
	return TotalSoma;
}

float subtracao (float sub1, float sub2){
	float TotalSub;
	
	TotalSub = sub1 - sub2;
	
	return TotalSub;
}

float divisao (float div1, float div2){
	float TotalDiv;
	
	TotalDiv = div1 / div2;
	
	return TotalDiv;
}


float multiplicacao(float mult1, float mult2){
	float totalmult;
	
	totalmult = mult1 * mult2;
	
	return totalmult;
	
}
